public class Sarsur {

    private int sarsurCapacity;

    Sarsur(int sarsurCapacity) {
        this.sarsurCapacity = sarsurCapacity;

    }

    public int getSarsurCapacity() {
        return this.sarsurCapacity;
    }

    public void decreaseSarsurCapacity() {
        sarsurCapacity--;
    }
}
